
class Animal:
    def run(self):
        return 'running!'

    def jump(self):
        return 'jumping!'


class Dog(Animal):
    def speak(self):
        return 'bark!'

    def swim(self):
        return 'swimming'

    def fetch(self):
        return 'fetching!'


class Cat(Animal):
    def speak(self):
        return 'meow!'



